package com.intercom.main;


/**
 * The Class Customer.
 * @author Yasith Lokuge
 */
public class Customer implements Comparable<Customer> {

	/** The name. */
	private String name;

	/** The user id. */
	private int userId;

	/**
	 * Gets the name.
	 *
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * Sets the name.
	 *
	 * @param name the new name
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * Gets the user id.
	 *
	 * @return the user id
	 */
	public int getUserId() {
		return userId;
	}

	/**
	 * Sets the user id.
	 *
	 * @param userId the new user id
	 */
	public void setUserId(int userId) {
		this.userId = userId;
	}

	/* (non-Javadoc)
	 * @see java.lang.Comparable#compareTo(java.lang.Object)
	 */
	public int compareTo(Customer compareCustomer) {

		int compareId = ((Customer) compareCustomer).getUserId();
		// ascending order
		return this.userId - compareId;

	}
}
